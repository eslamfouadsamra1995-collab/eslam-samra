export const parseCSV = (csvText: string): SensorDataPoint[] => {
  const lines = csvText.trim().split('\n');
  const data: SensorDataPoint[] = [];

  // Regex to check if a line starts with a date (handling quotes)
  // Format: "MM/dd/yyyy HH:mm:ss" or MM/dd/yyyy...
  const dateRegex = /^"?\d{1,2}\/\d{1,2}\/\d{4}/;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    
    // Skip header lines or empty lines by checking for date pattern
    if (!dateRegex.test(line)) {
      continue;
    }

    // Robust CSV split: Split by comma only if not inside quotes
    const cols = line.split(/,(?=(?:(?:[^"]*"){2})*[^"]*$)/);

    if (cols.length < 10) continue;

    // Helper to clean quotes and parse float
    const cleanStr = (val: string) => val ? val.replace(/^"|"$/g, '').trim() : '';
    const clean = (val: string) => {
      const s = cleanStr(val);
      const n = parseFloat(s);
      return isNaN(n) ? 0 : n;
    };

    try {
      const dateStr = cleanStr(cols[0]);
      const timestamp = new Date(dateStr).getTime();

      // Check for Invalid Date
      if (isNaN(timestamp)) continue;

      // Mapping based on the user's specific CSV structure:
      // 0: Date, 1: AmbTemp, 2: Press, 3: Hum, 4: SurfTemp, 5: Acoustics, 6: Battery, 
      // 7: Vel X, 8: Vel Y, 9: Vel Z
      data.push({
        timestamp,
        dateStr,
        ambientTemp: clean(cols[1]),
        pressure: clean(cols[2]),
        humidity: clean(cols[3]),
        surfaceTemp: clean(cols[4]),
        acoustics: clean(cols[5]),
        battery: clean(cols[6]),
        velX: clean(cols[7]),
        velY: clean(cols[8]),
        velZ: clean(cols[9]),
      });
    } catch (e) {
      console.warn("Failed to parse line:", line, e);
    }
  }

  return data.sort((a, b) => a.timestamp - b.timestamp);
};

